    private void approveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveBtnActionPerformed
        
        if(applicationIdField.getText().equals("")) {errorLabel.setText("Enter Application Id!");}
        
        int applicationId = Integer.parseInt(applicationIdField.getText());
                
        // Get approval date:
        long time = System.currentTimeMillis();
        java.sql.Date approvalDate = new java.sql.Date(time);
        
        jdbc = new JDBC();
        
        //Approve loan
        jdbc.approveLoanApplication(applicationId, approvalDate);
        
        // Update Loan Account Balance:
        int stuNum = jdbc.getStudentNumber(applicationId);
        double amount = jdbc.getLoanAmount(applicationId);        
        jdbc.updateAccountBalance(stuNum, amount);
        
        //Update Loan Date
        jdbc.updateLoanDate(stuNum,approvalDate);
        
        // Get name of user:
        String name = jdbc.getStudentName(stuNum);
        
        // Success message
        JOptionPane.showMessageDialog(null, "Loan Application approved! Amount of "+amount+" credited to "+name+".");
        
        //Populate New Applications Table:
        applications = jdbc.returnApplications();
        applicationsTable.setModel(DbUtils.resultSetToTableModel(applications));
        
    }//GEN-LAST:event_approveBtnActionPerformed

