    private void updateBalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBalBtnActionPerformed

        int studentNumber = Integer.parseInt(idField.getText());
        double amount = Double.parseDouble(amountField.getText());
        
        // Update balance
        double currentBalance = jdbc.getLoanBalance(studentNumber);
        double newBalance = currentBalance - amount;
        jdbc.makeLoanPayment(studentNumber, newBalance);
        
        // Get name of user:
        String name = jdbc.getStudentName(studentNumber);
        
        // Success message
        JOptionPane.showMessageDialog(null, "Loan payment of P"+amount+" made by "+name+". New loan balance: P"+newBalance+".");
        
    }//GEN-LAST:event_updateBalBtnActionPerformed

